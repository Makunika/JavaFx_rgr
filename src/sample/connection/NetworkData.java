package sample.connection;

public class NetworkData {
    private int code;
    private String text;

    public void setCode(int code) {
        this.code = code;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getText() {
        return text;
    }

    public int getCode() {
        return code;
    }
}
